package br.com.abc.introducao.MJVNJ.A2_Arrays;

public class Arrays03 {
    public static void main(String[] args) {

        String[] nome = new String[3];
        nome[0] = "Goku";
        nome[1] = "Seya";
        nome[2] = "Luffy";

        for (int i = 0; i < 3; i++){
            System.out.println(nome[i]);
        }

    }
}
